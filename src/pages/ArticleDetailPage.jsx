export default function ArticleDetailPage() {
    const { slug } = useParams();
    const article = articles[slug];

    if (!article) {
        return (
            <div className="min-h-screen flex flex-col bg-[#051C2C]">
                <Header />
                <div className="flex-1 flex items-center justify-center">
                    <div className="text-center">
                        <h1 className="text-4xl font-light text-white mb-4">Article Not Found</h1>
                        <Link to="/articles" className="text-gray-400 hover:text-white transition-colors">
                            ← Back to Articles
                        </Link>
                    </div>
                </div>
                <Footer />
            </div>
        );
    }

    // Parse markdown-style content to JSX
    const renderContent = (content) => {
        const lines = content.trim().split('\n');
        const elements = [];
        let currentParagraph = [];

        lines.forEach((line, idx) => {
            if (line.startsWith('# ')) {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
                elements.push(<h1 key={idx} className="text-4xl font-light text-white mb-8 mt-12">{line.substring(2)}</h1>);
            } else if (line.startsWith('## ')) {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
                elements.push(<h2 key={idx} className="text-3xl font-light text-white mb-6 mt-10">{line.substring(3)}</h2>);
            } else if (line.startsWith('### ')) {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
                elements.push(<h3 key={idx} className="text-2xl font-medium text-white mb-4 mt-8">{line.substring(4)}</h3>);
            } else if (line.startsWith('- ') || line.startsWith('* ')) {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
                elements.push(<li key={idx} className="text-gray-300 mb-2 ml-6">{line.substring(2)}</li>);
            } else if (line.match(/^\d+\./)) {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
                elements.push(<li key={idx} className="text-gray-300 mb-2 ml-6 list-decimal">{line.substring(line.indexOf('.') + 2)}</li>);
            } else if (line.startsWith('**') && line.endsWith('**')) {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
                elements.push(<p key={idx} className="text-white font-medium mb-3 mt-6">{line.replace(/\*\*/g, '')}</p>);
            } else if (line.trim() === '') {
                if (currentParagraph.length > 0) {
                    elements.push(<p key={`p-${idx}`} className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
                    currentParagraph = [];
                }
            } else {
                currentParagraph.push(line);
            }
        });

        if (currentParagraph.length > 0) {
            elements.push(<p key="final-p" className="text-gray-300 leading-relaxed mb-6">{currentParagraph.join(' ')}</p>);
        }

        return elements;
    };

    return (
        <div className="min-h-screen flex flex-col bg-[#051C2C]">
            <Header />

            {/* Hero Image */}
            <div className="w-full h-96 overflow-hidden">
                <img
                    src={article.image}
                    alt={article.title}
                    className="w-full h-full object-cover"
                />
            </div>

            {/* Article Content */}
            <article className="flex-1 py-16">
                <div className="container mx-auto px-8 max-w-4xl">
                    {/* Back Link */}
                    <Link to="/articles" className="text-gray-400 hover:text-white transition-colors mb-8 inline-block">
                        ← Back to Articles
                    </Link>

                    {/* Article Header */}
                    <div className="mb-12">
                        <div className="flex items-center gap-4 mb-4">
                            <span className="text-xs uppercase tracking-wider text-gray-500">{article.category}</span>
                            <span className="text-xs text-gray-600">•</span>
                            <span className="text-xs text-gray-500">{article.readTime}</span>
                        </div>
                        <h1 className="text-5xl font-light tracking-tight text-white mb-6">{article.title}</h1>
                        <div className="flex items-center gap-4 text-sm text-gray-400">
                            <span>By {article.author}</span>
                            <span>•</span>
                            <span>{article.date}</span>
                        </div>
                    </div>

                    {/* Article Body */}
                    <div className="prose prose-invert max-w-none">
                        {renderContent(article.content)}
                    </div>

                    {/* Related Articles */}
                    <div className="mt-16 pt-16 border-t border-white/10">
                        <h3 className="text-2xl font-light text-white mb-8">Related Articles</h3>
                        <div className="grid md:grid-cols-2 gap-6">
                            {Object.entries(articles)
                                .filter(([key]) => key !== slug)
                                .slice(0, 2)
                                .map(([key, relatedArticle]) => (
                                    <Link
                                        key={key}
                                        to={`/articles/${key}`}
                                        className="group"
                                    >
                                        <div className="aspect-video overflow-hidden bg-[#042440] mb-4">
                                            <img
                                                src={relatedArticle.image}
                                                alt={relatedArticle.title}
                                                className="w-full h-full object-cover filter grayscale group-hover:grayscale-0 transition-all duration-700"
                                            />
                                        </div>
                                        <h4 className="text-lg font-medium text-white group-hover:text-gray-300 transition-colors">
                                            {relatedArticle.title}
                                        </h4>
                                        <p className="text-sm text-gray-500 mt-2">{relatedArticle.author} • {relatedArticle.date}</p>
                                    </Link>
                                ))}
                        </div>
                    </div>
                </div>
            </article>

            <Footer />
        </div>
    );
}
